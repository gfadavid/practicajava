/*
    Paquetes Java y API

    Un paquete en Java se usa para agrupar clases relacionadas. Piense en ello como una carpeta en un directorio de archivos.
    Usamos paquetes para evitar conflictos de nombres y para escribir un código mejor mantenible. Los paquetes se dividen en dos
    categorías:

     Paquetes integrados (paquetes de la API de Java)
     Paquetes definidos por el usuario (cree sus propios paquetes)

    Paquetes integrados

    La API de Java es una biblioteca de clases preescritas, de uso gratuito, incluidas en el entorno de desarrollo de Java.
    La biblioteca contiene componentes para administrar entradas, programar bases de datos y mucho más.
    La biblioteca se divide en paquetes y clases. Lo que significa que puede importar una sola clase (junto con sus métodos
    y atributos) o un paquete completo que contenga todas las clases que pertenecen al paquete especificado.
    Para usar una clase o un paquete de la biblioteca, debe usar la palabra clave de importación:

    En el ejemplo anterior, java.util es un paquete, mientras que Scanner es una clase del paquete java.util.

    Para usar la clase Scanner, cree un objeto de la clase y use cualquiera de los métodos disponibles que se encuentran
    en la documentación de la clase Scanner. En nuestro ejemplo, usaremos el método nextLine(), que se usa para leer una
    línea completa

Importar un paquete

Hay muchos paquetes para elegir. En el ejemplo anterior, usamos la clase Scanner del paquete java.util. Este paquete
también contiene instalaciones de fecha y hora, generador de números aleatorios y otras clases de utilidad.

Para importar un paquete completo, finalice la oración con un signo de asterisco (*). El siguiente ejemplo importará TODAS las
clases en el paquete java.util
 */
